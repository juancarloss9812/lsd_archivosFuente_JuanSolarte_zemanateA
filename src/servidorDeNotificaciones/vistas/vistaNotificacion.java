    public void notificarTabla(Pedido objPedido) {
        int limite = pos + objPedido.getListaHamburguesas().size();
        int contador = 0;
        String Matrix[][] = new String[limite][4];

        for (int i = 0; i < pos; i++) {
            Matrix[i][0] = atrMatrix[i][0];
            Matrix[i][1] = atrMatrix[i][1];
            Matrix[i][2] = atrMatrix[i][2];
            Matrix[i][3] = atrMatrix[i][3];
        }

        for (int i = 0; i < objPedido.getListaHamburguesas().size(); i++) {
            Matrix[pos][0] = objPedido.getMesa();
            Matrix[pos][1] = "" + (i + 1);

            switch (objPedido.getListaHamburguesas().get(i).getTipo()) {
                case 'P':
                    Matrix[pos][2] = "PEQUEÃ‘A";
                    break;
                case 'M':
                    Matrix[pos][2] = "MEDIANA";
                    break;
                case 'G':
                    Matrix[pos][2] = "GRANDE";
                    break;
                default:
                    break;
            }

            Matrix[pos][3] = "" + objPedido.getListaHamburguesas().get(i).getCantidadIngredientesExtra();
            pos++;

        }
        tblNotificaciones.setModel(new javax.swing.table.DefaultTableModel(
                Matrix,
                new String[]{
                    "# MESA", "#HAMBURGUESA", "TIPO", "INGREDIENTES EXTRA"
                }
        ));
        atrMatrix = Matrix;
    }

